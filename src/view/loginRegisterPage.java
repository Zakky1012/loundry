    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        String passwordRegister         = jPasswordFieldRegister.getText();
        String repeatPasswordRegister   = jPasswordFieldRepeatRegister.getText();
        String usernameRegister         = usernameTextFieldRegister.getText();
        try {
            
            if (passwordRegister.equals("") || repeatPasswordRegister.equals("") || usernameRegister.equals("")) {
                
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong", "pesan" , JOptionPane.ERROR_MESSAGE);
                
            } else if (!passwordRegister.equals(repeatPasswordRegister)){
                
                JOptionPane.showMessageDialog(this, "Password harus sama", "pesan", JOptionPane.ERROR_MESSAGE);
                
            } else {
                
                String hashPasswordMd5  = hashPasswordMD5(passwordRegister);
                
                String query            = "insert into tabel_user (username, password) values (?, ?)";
                PreparedStatement pst   = conn.prepareStatement(query);
                pst.setString(1, usernameRegister);
                pst.setString(2, hashPasswordMd5);
                
                int affectedRows = pst.executeUpdate();
                if (affectedRows > 0) {
                    JOptionPane.showMessageDialog(this, "Register sudah berhasil silahkan anda login", "pesan", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "terjadi kesalahan SQL dan Kesalahan lain", "pesan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSubmitActionPerformed

