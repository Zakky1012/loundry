    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextUsernameLogin.getText();
        String password = new String (jPasswordFieldLogin.getPassword());
        String passwordHash = hashPasswordMD5(password);
        
        try {
            
            if (username.equals("") || password.equals("")) {
                JOptionPane.showMessageDialog(this, "form inputan tidak boleh kosong", "pesan", JOptionPane.ERROR_MESSAGE);
            } else {
                // password hash
                
                String sql              = "select * from tabel_user where username = ? and password = ? ";
                PreparedStatement pst   = conn.prepareStatement(sql);
                pst.setString(1, username);
                pst.setString(2, passwordHash);
                
                ResultSet res = pst.executeQuery();
                if (res.next()) {
                    
                    // apabila berhasil login                 
                    JOptionPane.showMessageDialog(this, "login berhasil", "pesan", JOptionPane.INFORMATION_MESSAGE);
                    mainUtama main = new mainUtama();
                    main.setVisible(true);
                    
                } else {
                    
                    JOptionPane.showMessageDialog(this, "username atau password salah", "pesan", JOptionPane.ERROR_MESSAGE);
                            
                }
                
            }
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan Sql atau Kesalahan lain", "pesan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

