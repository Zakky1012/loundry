    private void getDataCustomer(){
        DefaultTableModel model     = (DefaultTableModel) CustomerTable.getModel();
        // Menghapus semua baris yang ada di model
        model.setRowCount(0);

        // Menambahkan kolom hanya jika belum ada
        if (model.getColumnCount() == 0) {
            model.addColumn("No");
            model.addColumn("Kode Kostumer");
            model.addColumn("Nama");
            model.addColumn("No Telp");
            model.addColumn("Alamat");
        }
        try {
            int no = 1;
            String sql                  = "select * from `tabel_custumer` ";
            stm                         = conn.createStatement();
            ResultSet res               = stm.executeQuery(sql);
            
            // Menampilkan data dari database           
            while (res.next()) {
                model.addRow(new Object[]{
                    no++,
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4)}
                
                );
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan", "pesan", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
