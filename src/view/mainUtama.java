    private void simpanCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanCustomerMouseClicked
        // sudah beres simpan data costumer        
        
        String nama          = jTextFieldCustomer.getText();
        String noTelp        = jTextFieldNoTelp.getText();
        String alamat        = jTextAreaAlamat.getText();
        String kodeCustomer  = jTextFieldIdCustomer.getText();

        try {
            if (nama.equals("") || noTelp.equals("") || alamat.equals("")) {
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong", "pesan", JOptionPane.ERROR_MESSAGE);
            } else {
                String query = "INSERT INTO tabel_custumer (kode_customer, nama, no_telp, alamat) VALUES (?, ?, ?, ?)";
                try (PreparedStatement pst = conn.prepareStatement(query)) {
                    pst.setString(1, "CS" + kodeCustomer);
                    pst.setString(2, nama);
                    pst.setString(3, noTelp);
                    pst.setString(4, alamat);
                    pst.executeUpdate();
                    System.out.println(pst);
                    JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "pesan", JOptionPane.INFORMATION_MESSAGE);
                    getDataCustomer();
                    reset();
                    stm.close();
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan SQL: " + e.getMessage(), "pesan", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "pesan", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_simpanCustomerMouseClicked

