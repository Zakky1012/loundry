    private void cariCostumerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariCostumerActionPerformed
        String cariCostumer                   = cariDataCostumer.getText();
        DefaultTableModel model               = (DefaultTableModel) CustomerTable.getModel();
        model.getDataVector().removeAllElements();
        try {
            
            String sql                        = "SELECT * from tabel_custumer where kode_customer = ?";
                PreparedStatement pst = conn.prepareStatement(sql, 
                        ResultSet.TYPE_SCROLL_INSENSITIVE, 
                        ResultSet.CONCUR_READ_ONLY);
                
                pst.setString(1, cariCostumer);
                rs = pst.executeQuery();
                rs.last();
                int rowCount = rs.getRow();
                rs.beforeFirst();
                int nomor = 1;
                if (rowCount > 0) {
                    while (rs.next()) {                    
                        String kodeCustomer        = rs.getString("kode_customer");
                        String nama                = rs.getString("nama");
                        String noTelp              = rs.getString("no_telp");
                        String alamat              = rs.getString("alamat");
                        
                        Object[] data = {nomor, kodeCustomer, nama, noTelp, alamat };
                        model.addRow(data);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
                }
            rs.close();
            pst.close();
        } catch (SQLException e) {
            System.out.println("terdapat query yang salah " + e.getMessage());
        }
    }//GEN-LAST:event_cariCostumerActionPerformed

